<?php
  // Operator penugasan adalah operator yang berfungsi untuk memberikan nilai ke variabel

  $variable = 50;
  var_dump($variable);
  echo "<hr>";
  
  $variable += 3; // $variable = $variable + 3;
  var_dump($variable);
  echo "<hr>";
  
  $variable *= 3; // $variable = $variable * 3;
  var_dump($variable);
  echo "<hr>";

  $variable /= 3; // $variable = $variable / 3;
  var_dump($variable);
  echo "<hr>";

  $variable %= 3; // $variable = $variable % 3;
  var_dump($variable);
  echo "<hr>";

  $variable **= 3; // $variable = $variable ** 3;
  var_dump($variable);
  echo "<hr>";